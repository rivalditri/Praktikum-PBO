    private int getIncreaseNim()
    {
        int increaseNumber = 0;
        try{
            sql = "SELECT NIM FROM data_mahasiswa ORDER BY id_data DESC";
            conn =(Connection)Database.configDB()  ;                 //MENGKONEKSIKAN KEDATABASE DARI KELAS DATABASE METHOD CONFIGDB
            pstm = conn.prepareStatement(sql);                         //MEMBUAT STATEMENT MYSQL
            res = pstm.executeQuery();
            
            if(res.next())
            {
                int Nim = res.getInt(1);
                increaseNumber = Nim + 1;                
            }
        }
        catch(SQLException e)
        {
        }
        return increaseNumber;
    }
    
    private String getRandomClass()
    {
        String [] arrayclass = {"A","B","C","D"};
        Random random = new Random();
        int randomClass = random.nextInt(4);
        return arrayclass[randomClass];
    }
    
    private void simpanData()
    {
        nama = txtnama.getText();
        ttl = txtTtl.getText();
        jenisKelamin = cbJK.getSelectedItem().toString();
        alamat = txtalm.getText();
        programStudi = txtPs.getText();
        kelas = getRandomClass();                                 //MENGAMBIL NILAI KELAS DARI METHOD RANDOM
        nim = getIncreaseNim();
        
        try {                               
           if (nama.equals("")                                    //PERCABANGAN JIKA TEXTFIELD NAMA KOSONG
               || ttl.equals("")                                  //PERCABANGAN JIKA TEXTFIELD TTL KOSONG
               || jenisKelamin.equals("")                         //PERCABANGAN JIKA TEXTFIELD JENIS KELAMIN KOSONG
               || programStudi.equals("")                         //PERCABANGAN JIKA COMBOBOX PROGRAMSTUDI KOSONG
               || alamat.equals("")    )                          //PERCABANGAN JIKA TEXTFIELD ALAMAT KOSONG
            {
                JOptionPane.showMessageDialog
                    (this, "Data tidak boleh kosong", "error", JOptionPane.ERROR_MESSAGE);     //AKAN KELUAR TAMPILAN JOPTION ERROR
                emptyData();
            } 
            else
            {     //PERCABANGAN JIKA tidak kosong
                sql = "INSERT INTO data_mahasiswa  VALUES "               //MEMASUKKAN NILAI KE DALAM DATABASE
                        + "('"+nim+"' ,"                                 //KOLOM 1 PADA DATABASE DIAMBIL DARI INCREASENIM                           
                        + "'"+nama+"' , "                                //KOLOM 2 PADA DATABASE DIAMBIL DARI TEXTFIELD NAMA
                        + "'"+ttl+"' , "                                 //KOLOM 3 PADA DATABASE DIAMBIL DARI TEXTFIELD TTL
                        + "'"+jenisKelamin+"' , "                        //KOLOM 4 PADA DATABASE DIAMBIL DARI COMBOBOX JENISKELAMIN
                        + "'"+programStudi+"' ,"                         //KOLOM 5 PADA DATABASE DIAMBIL DARI TEXTFIELD PROGRAM STUDI
                        + "'"+alamat+"' ,"                               //KOLOM 6 PADA DATABASE DIAMBIL DARI TEXTFIELD PROGRAMSTUDI
                        + "'"+kelas+"' ,"                                //KOLOM 7 PADA DATABASE DIAMBIL DARI RANDOM KELAS
                        + "'"+getId()+"' )";                              
                conn =(Connection)Database.configDB()  ;                 //MENGKONEKSIKAN KEDATABASE DARI KELAS DATABASE METHOD CONFIGDB
                pstm = conn.prepareStatement(sql);                                           //MEMBUAT STATEMENT MYSQL
                pstm.execute();                                                              //MENGEKSEKUSI STATEMENT
                JOptionPane.showMessageDialog(null,"Registrasi Berhasil");                   //MENAMPILKAN JOPTION "REGISTRASI BERHASIL"
                toRegister();
                
            } 
        }catch(HeadlessException | SQLException e){                                              //JIKA TERJADI ERROR DALAM PENGKONEKSIAN DATABASE
          JOptionPane.showMessageDialog(this, e.getMessage());                                   //MENAMPILKAN ERRORNYA DIMANA
         } 
    }
    
