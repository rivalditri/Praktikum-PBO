    public int getId() {
        try
        {
            sql = "SELECT id_data FROM data_mahasiswa ORDER BY id_data DESC";
            conn =(Connection)Database.configDB();
            pstm = conn.prepareStatement(sql);
            res = pstm.executeQuery();
            
            if(res.next())
            {
                int Id = res.getInt(1);
                id = Id+1;
            }
        }
        catch(SQLException e){
            
        }
        
        return id;
    }
    
