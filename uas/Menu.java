    //update table dari jadwalbaru yang telah di edit
    private void updateTable(){
        // perulangan i = index kolom
        // perulangan j = index baris
        for (int i = 0; i < 5; i++) {
            for (int j = 0; j < 3; j++) {
                tbJadwal.setValueAt(jadwalbaru[i][j], j, i+1);
                tbJadwal.setValueAt(jadwal.getContentJadwal(1,3), 3, i+1);
            }
        }
    }
